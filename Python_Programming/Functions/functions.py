# function with parameters/arguments but no return value
def myfuncc(name):
     print("Hello " + name)

myfuncc("Alice") # we are passing an argument "Alice" to the function
myfuncc("Bob") # we are passing an argument "Bob" to the function

# function with parameters/arguments and return value
def cal(a, b):
     return a+b
res = cal(5, 10)  # we are passing 5 and 10 as arguments to the function
print('sum is: ', res)
print('sum is: ', cal(50, 120)) # directly printing the return value of the function

